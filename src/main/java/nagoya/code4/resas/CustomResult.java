package nagoya.code4.resas;

public class CustomResult {
	private String customHouseCode;

	private String customHouseName;

	private String prefCode;

	private String prefName;

	public String getCustomHouseCode() {
		return customHouseCode;
	}

	public void setCustomHouseCode(String customHouseCode) {
		this.customHouseCode = customHouseCode;
	}

	public String getCustomHouseName() {
		return customHouseName;
	}

	public void setCustomHouseName(String customHouseName) {
		this.customHouseName = customHouseName;
	}

	public String getPrefCode() {
		return prefCode;
	}

	public void setPrefCode(String prefCode) {
		this.prefCode = prefCode;
	}

	public String getPrefName() {
		return prefName;
	}

	public void setPrefName(String prefName) {
		this.prefName = prefName;
	}

	

}
